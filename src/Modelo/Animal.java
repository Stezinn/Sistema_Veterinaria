package src.Modelo;

public class Animal {


        private String nome;
        private String raca;
        private String dono;

        public String getNome() {
            return nome;
        }

        public void setNome(String nome) {
            this.nome = nome;
        }

        public String getRaca() {
            return raca;
        }

        public String getDono() {
            return dono;
        }

        public void setDono(String dono) {
            this.dono = dono;
        }

        public void setRaca(String raca) {
            this.raca = raca;

        }
    }


